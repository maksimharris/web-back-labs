def put_film(id):
    if 0 <= id < len(films):
        film = request.get_json()
        
        if not film:
            return {"error": "No data provided"}, 400
        
        # Валидация данных
        errors = validate_film_data(film)
        if errors:
            return errors, 400
        
        # Обновляем фильм
        films[id] = film
